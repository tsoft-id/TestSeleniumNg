package com.tsoftlatam.automatizacion.entidad;


public class Entidad {
	private String colIdMoodle;
	private String colNombrePosicion;
	private int colIdPersona;
	private String colNombreApellidoUsuario;
	
	public Entidad(){
		
	}

	public String getColIdMoodle() {
		return colIdMoodle;
	}

	public void setColIdMoodle(String colIdMoodle) {
		this.colIdMoodle = colIdMoodle;
	}

	public String getColNombrePosicion() {
		return colNombrePosicion;
	}

	public void setColNombrePosicion(String colNombrePosicion) {
		this.colNombrePosicion = colNombrePosicion;
	}

	public int getColIdPersona() {
		return colIdPersona;
	}

	public void setColIdPersona(int colIdPersona) {
		this.colIdPersona = colIdPersona;
	}

	public String getColNombreApellidoUsuario() {
		return colNombreApellidoUsuario;
	}

	public void setColNombreApellidoUsuario(String colNombreApellidoUsuario) {
		this.colNombreApellidoUsuario = colNombreApellidoUsuario;
	}	
	
}
